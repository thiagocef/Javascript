let palavra = 'Marilucia'

console.log(palavra.substring(3)) //puxa a partir da terceira letra
console.log(palavra.substring(0, 3)) //puxa da primeira até a terceira letra
console.log(palavra.substring(2, 4)) //puxa da terceira até até a oitava letra
console.log(palavra.substring(0, palavra.length -1)) //pega da primeira letra até a penúltima 
console.log(palavra.charAt(palavra.length -1)) //última letra